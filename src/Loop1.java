public class Loop1 {
    public static void main(String[] args) {

        //while loop
        System.out.println("Using While loop: ");
        int count = 1;
        while(count!=5){
            System.out.println(count);
            count++;
        }

        //for loop
        System.out.println("Using For loop: ");
        for (count=1; count!= 5 ; count++){
            System.out.println(count);
        }
    }
}

